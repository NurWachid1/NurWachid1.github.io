            <br><p style="text-align: justify; text-indent: 50px">Beberapa kendala yang dihadapi dalam upaya menambah koleksi museum diantaranya masih banyaknya benda-benda yang masih dimiliki dikuasai pribadi, keluarga atau keturunan kraton dan kolektor serta beberapa di antaranya tersebar di beberapa wilayah bahkan luar negeri, khususnya di Belanda. Oleh karenanya upaya-upaya itu akan tetap dilakukan untuk menyelamatkan dan melestarikan benda-benda peninggalan bersejarah tersebut.</p>
            <br><p style="text-align: justify; text-indent: 50px">Dilakukan penghimpunan peralatan, benda-benda dan dokumen-dokumen bekas milik “Keraton Bangkalan” yang masih ada dan tersisa untuk bisa diurus dan dirawat. Selanjutnya benda-benda tersebut dikumpulkan dan dipindahkan dalam sebuah Gudang yang terletak di komplek Pemakaman Raja-Raja Bangkalan “Pesarean Aer Mata”. Desa Buduran Kec. Arosbaya. Pada tahun-tahun itu juga terbentuk sebuah Yayasan dengan nama “YAYASAN KONA”. Bersama dengan Yayasan ini yang diwakili oleh R.A. MOCH ANWAR TJAKRA ADIPOETRO, R.P. ABDOEL MADJID SURJOWINOTO DAN R.P.ABDOEL HAMID NOTODIREDJO Bupati Kepala Daerah Tingkat II Bangkalan saat itu, H.J. SOEDJAKI ( 1971 s.d 1976 ) dibantu staf beliau R.A. SALEH SOSROADIPOETRO dan ABD. RACHMAN bersepakat untuk memindah benda-benda tersebut ke tempat yang lebih representative. Atas dasar itulah maka pada tahun 1974 dibangunlah sebuah gedung di komplek Pendopo Kabupaten Bangkalan, JL. Letnan Abdullah No. 1 Bangkalan. Untuk mendukung keberadaan gedung tersebut dibangun pula pintu gerbang yang bentuk dan arsitekturnya serta ornamennya serupa dengan Bentar Makam Agung Arosbaya. Dengan disaksikan oleh GUSTI PEMBAYON, permaisuri R.A. ROESLANT TJAKRANINGRAT, pada tanggal 24 Juli 1975 benda-benda tersebut dipindah ke gededung baru dan sejak saat itu pemeliharaan dan perawatan benda-benda koleksi itu dilakukan oleh pemerintah Daerah “ MUSEUM DAERAH TINGKAT II bANGKALAN”.</p>
